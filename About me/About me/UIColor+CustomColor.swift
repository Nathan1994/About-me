import Foundation

extension UIColor{
    class func customGreenColor() -> UIColor {
        return UIColor(red: 184/255, green: 233/255, blue: 134/255, alpha: 1.0)
    }
    class func customYellowColor() -> UIColor {
        return UIColor(red: 248/255, green: 231/255, blue: 28/255, alpha: 1.0)
    }
    class func customPurpleColor() -> UIColor {
        return UIColor(red: 219/255, green: 104/255, blue: 243/255, alpha: 1.0)
    }
    class func customRedColor() -> UIColor {
        return UIColor(red: 249/255, green: 77/255, blue: 98/255, alpha: 1.0)
    }
    class func customBlueColor() -> UIColor {
        return UIColor(red: 125/255, green: 178/255, blue: 240/255, alpha: 1.0)
    }

}